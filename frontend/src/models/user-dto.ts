/**
 * DTO representation of a User.
 * @export
 * @interface UserDto
 */
export interface UserDto {
  /**
   *
   * @type {string}
   * @memberof UserDto
   */
  'id'?: string | null;
  /**
   *
   * @type {string}
   * @memberof UserDto
   */
  'firstName': string;
  /**
   *
   * @type {string}
   * @memberof UserDto
   */
  'lastName': string;
  /**
   *
   * @type {string}
   * @memberof UserDto
   */
  'institution': string;
  /**
   * @type {string}
   * @memberof UserDto
   */
  'username': string;

  /**
   * @type {string}
   * @memberof UserDto
   */
  'email': string;

  /**
   * @type {string}
   * @memberof UserDto
   */
  'password': string;
}



